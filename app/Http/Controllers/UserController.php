    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  \App\Models\User  $user
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, User $user)
    {
        $status = ($request->is_block) ? 'blocked' : 'Unblocked';
        if($this->userService->update($request, $user)){
            return back()->with('success', 'User '.$status.' successfully');
        }else{
            return back()->with('danger', 'Could not update user. Try again after some time.');
        }
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  \App\Models\User  $user
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        if($this->userService->destroy($id)){
            return back()->with('success', 'User deleted successfully');
        }else{
            return back()->with('danger', 'Could not delete user. Try again after some time.');
        }
    }
}
